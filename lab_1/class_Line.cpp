#include "class_Line.h"


namespace Line{

/***************************class Line********************************************************************************/
/*------------------------------------------------------------------------------------------------------------------*/
Line::Line(){}
/*------------------------------------------------------------------------------------------------------------------*/

/*------------------------------------------------------------------------------------------------------------------*/
Line::Line( double & kv, double & bv ){
  k = kv ;
  b = bv ;
}
/*------------------------------------------------------------------------------------------------------------------*/

/*------------------------------------------------------------------------------------------------------------------*/

/*------------------------------------------------------------------------------------------------------------------*/

/*------------------------------------------------------------------------------------------------------------------*/
double Line::K() const {
  return Line::k ;
}
/*------------------------------------------------------------------------------------------------------------------*/

/*------------------------------------------------------------------------------------------------------------------*/
double Line::B() const {
  return Line::b ;
}
/*------------------------------------------------------------------------------------------------------------------*/

/*------------------------------------------------------------------------------------------------------------------*/
void Line::_set( double & kv, double & bv ){
  k = kv ;
  b = bv ;
}
/*------------------------------------------------------------------------------------------------------------------*/

/*------------------------------------------------------------------------------------------------------------------*/
double Line::Point_x(){
  return ( - k / b ) ;
}
/*------------------------------------------------------------------------------------------------------------------*/

/*------------------------------------------------------------------------------------------------------------------*/
double Line::Point_y(){
  return b ;
}
/*------------------------------------------------------------------------------------------------------------------*/

/*------------------------------------------------------------------------------------------------------------------*/
int64_t Line::Prov( const Line & l ){
  if( ( k == l.k ) && ( b == l.b ) ) return -1 ;
  else if ( k == l.k ) return 0 ;
  return 1 ;
}
/*------------------------------------------------------------------------------------------------------------------*/
/***************************class Line********************************************************************************/

} //namespace Line
